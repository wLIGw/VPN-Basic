export const PayMethodWidget = () => {
    const [selected, setSelected] = useState<null | number>(null)
    const isMobile = useMediaQuery('(max-width: 768px)')
    const navigate = useNavigate() 

    const handleSelect = (index: number) => { 
        setSelected(index)
        setTimeout(() => {
            navigate('/?payment=success')
        }, 300)
    }

    return (
        <Box>
            <Box style={{ display: 'flex', flexDirection: 'column', gap: 8 }}>
                {methods.map((method, index) => (
                    <Box
                        key={index}
                        onClick={() => handleSelect(index)}
                        onMouseEnter={(e) => {
                            e.currentTarget.style.background = 'rgba(0,0,0,0.02)'
                        }}
                        onMouseLeave={(e) => {
                            e.currentTarget.style.background =
                                selected === index ? 'rgba(0,0,0,0.02)' : '#fff'
                        }}
                        style={{
                            display: 'flex',
                            alignItems: 'center',
                            gap: 12,
                            padding: '14px 16px',
                            cursor: 'pointer',
                            border: ' 2px solid rgba(150, 150, 150, 0.2)',
                            borderRadius: 16,
                            background: selected === index ? 'rgba(0,0,0,0.02)' : '#fff',
                            transition: 'background 0.15s ease'
                        }}
                    >
                        {method.icon}
                        <Box>
                            <Text style={{ fontWeight: 700, fontSize: 14, color: '#141820' }}>
                                {method.title}
                            </Text>
                            <Text style={{ fontWeight: 500, fontSize: 12, color: '#969696' }}>
                                {method.subtitle}
                            </Text>
                        </Box>
                    </Box>
                ))}
            </Box>
            <Box mt={12} style={{ textAlign: 'center' }}>
                <Text style={{ fontWeight: 500, fontSize: isMobile ? 10 : 12, color: '#969696' }}>
                    Оплачивая здесь, вы соглашаетесь с{' '}
                    <span
                        style={{
                            textDecoration: 'underline',
                            cursor: 'pointer',
                            color: '#1C2029'
                        }}
                    >
                        условиями сервиса
                    </span>
                </Text>
            </Box>
        </Box>
    )
}
